<h2>WORLD FISHERIES DAY</h2>
Today, 21 November, fishing communities around the globe are marking World Fisheries Day.

Despite the growth of aquaculture, small-scale capture fisheries will continue to supply most of the fish consumed in much
 of the developing world in the coming decades. The majority of these fisheries are small-scale, operating 
in rivers, lakes, wetlands, coral reefs and estuaries in coastal seas, and provide livelihoods for millions of people.
