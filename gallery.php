<h2>UNDERWATER MUSEUM TO COME IN PUDUCHERRY</h2>
The Indian Navy has joined hands with the government, National Institute of Ocean Technology, National Center for Coastal Research, 
and an esteemed NGO named PondyCan on this project. It has donated INS Cuddalore, a decommissioned minesweeper which has covered more than
 30,000 nautical miles in the last 30 years, for the purpose of being revamped into a sea museum.
 The vehicle will be deployed in the seabed about 7 km from the coast of Pondicherry at a depth of 26 m.
