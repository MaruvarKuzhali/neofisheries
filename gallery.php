<h2>NARALI PURNIMA</h2>
Narali Purnima, also known as ‘Coconut Day’ is an important festival celebrated majorly by Hindus in the western coastal regions of India. 
It is observed on the ‘Purnima’ (full moon day) in the month of ‘Shravana’ in the Hindu calendar and therefore referred as ‘Shravana Purnima’.
 For those following Gregorian calendar, this occurrence falls between the months of July-August.
 Narali Purnima is observed with great zeal and fervour in Maharashtra and adjoining Konkani regions.
</div>
